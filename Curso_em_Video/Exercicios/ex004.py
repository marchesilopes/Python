# Faça um programa que leia algo pelo teclado e mostre na tela o seu tipo primitivo e todas as informações possíveis
# sobre ele.
n = input('Digite algo: ')
print(n.isnumeric())
print(n.isalnum())
print(n.isalpha())
print(n.isascii())
print(n.isdecimal())
print(n.isdigit())
print(n.islower())
print(n.isupper())
print(n.istitle())
print(n.isspace())
print(n.isprintable())
