# OPERAÇÕES ARITMÉTICAS
# + = Adição
# - = Subtração
# * = Multiplicação
# / = Divisão
# ** = Potência
# // = Divisão inteira
# % = Resto da divisão

print(5+2)
print(5-2)
print(5*2)
print(5/2)
print(5**2)
print(5//2)
print(5%2)

## ORDEM DE PRECEDÊNCIA
# 1 - ()
# 2 - **
# 3 - * / // %
# 4 - + - 

print(5 + 3 * 2)
print(3 * 5 + 4 ** 2)
print(3 * (5 + 4) ** 2)